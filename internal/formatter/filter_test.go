package formatter

import (
	"encoding/json"
	"testing"
	"time"

	"github.com/capybara-translation/ccrec/internal/parser"
)

func makeRecord(typ string, role string, content string) *parser.Record {
	return &parser.Record{
		Type:      typ,
		Timestamp: time.Now(),
		Message: &parser.Message{
			Role:    role,
			Content: json.RawMessage(`"` + content + `"`),
		},
	}
}

func TestFilterRecords_KeepsUserAndAssistant(t *testing.T) {
	records := []*parser.Record{
		makeRecord("user", "user", "hello"),
		makeRecord("assistant", "assistant", "hi there"),
	}
	filtered := FilterRecords(records, false)
	if len(filtered) != 2 {
		t.Fatalf("got %d, want 2", len(filtered))
	}
}

func TestFilterRecords_ExcludesNonMessageTypes(t *testing.T) {
	records := []*parser.Record{
		makeRecord("user", "user", "hello"),
		makeRecord("file-history-snapshot", "", ""),
		makeRecord("progress", "", ""),
		makeRecord("system", "", "some system info"),
		makeRecord("summary", "", "summary text"),
	}
	filtered := FilterRecords(records, false)
	if len(filtered) != 1 {
		t.Fatalf("got %d, want 1 (only user message)", len(filtered))
	}
}

func TestFilterRecords_ExcludesEmptyContent(t *testing.T) {
	records := []*parser.Record{
		makeRecord("assistant", "assistant", ""),
		makeRecord("assistant", "assistant", "   "),
	}
	filtered := FilterRecords(records, false)
	if len(filtered) != 0 {
		t.Fatalf("got %d, want 0", len(filtered))
	}
}

func TestFilterRecords_ExcludesMetaMessages(t *testing.T) {
	rec := makeRecord("user", "user", "meta message")
	rec.IsMeta = true
	filtered := FilterRecords([]*parser.Record{rec}, false)
	if len(filtered) != 0 {
		t.Fatalf("got %d, want 0", len(filtered))
	}
}

